# Найти заданное число в матрице
mat = [[15,20,40,85],[20,35,80,95],[30,55,95,105],[40,80,100,120]]
x=55
c=0
while c==0:
	for str in mat:
		for elem in str:
			if elem == x:
				print ('Найдено число ', x)
				c=1
				break
			
		
	if c==0: 
		print ('Числа ',x,' нет в матрице')	
		c=1